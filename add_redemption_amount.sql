-- ============================================================
-- เพิ่มฟิลด์ ยอดสินไถ่ (contract_redemption_amount) ใน cases
-- สำหรับสัญญาขายฝาก — จำนวนเงินที่ลูกหนี้ต้องจ่ายเพื่อไถ่ทรัพย์คืน
-- สูตร: ยอดสินไถ่ = วงเงินขายฝาก × (1 + ดอกเบี้ย% × ปี)
-- ============================================================
-- หมายเหตุ: redemption_amount ใน debtor_accounting คือค่าบริการ
--           ต่างจาก contract_redemption_amount ที่เป็นยอดสินไถ่จริง
-- ============================================================

ALTER TABLE `cases`
  ADD COLUMN IF NOT EXISTS `contract_redemption_amount`
    decimal(15,2) DEFAULT NULL
    COMMENT 'ยอดสินไถ่ตามสัญญาขายฝาก (วงเงิน + ดอกเบี้ยตลอดสัญญา)'
  AFTER `contract_end_date`;
